/**
 * DukeException class.
 * Used to represent the exceptions related to Duke.
 *
 * @author KelvinSoo
 * @version Level-5
 *
 */
public class DukeException extends Exception{
    public DukeException(String errorMessage) {
        super(errorMessage);
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
