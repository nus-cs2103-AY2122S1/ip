package duke;

/**
 * This class encapsulates an exception specific to Duke.
 *
 * @author Kleon Ang
 */
public class DukeException extends Exception {
    /**
     * Constructor for a DukeException.
     *
     * @param message A message describing the DukeException that occurred.
     */
    public DukeException(String message) {
        super(message);
    }
}
